export type OrgHierarchy = {
    [영업본부: string]: {
        [부서명: string]: {
            [팀명: string]: string[]
        }
    }
}

export function buildOrgHierarchy(): OrgHierarchy {
    const hierarchy: OrgHierarchy = {}
    for (const [영업본부, 부서명, 팀명, 매장명] of ORG_RAW_DATA) {
        if (!hierarchy[영업본부]) hierarchy[영업본부] = {}
        if (!hierarchy[영업본부][부서명]) hierarchy[영업본부][부서명] = {}
        if (!hierarchy[영업본부][부서명][팀명]) hierarchy[영업본부][부서명][팀명] = []
        if (!hierarchy[영업본부][부서명][팀명].includes(매장명)) {
            hierarchy[영업본부][부서명][팀명].push(매장명)
        }
    }
    return hierarchy
}

export function buildFlatData() {
    return ORG_RAW_DATA.map(([영업본부, 부서명, 팀명, 매장명]) => ({ 영업본부, 부서명, 팀명, 매장명 }))
}
